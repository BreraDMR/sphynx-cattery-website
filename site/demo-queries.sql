-- SELECT — вибірка всіх записів
SELECT * FROM requests ORDER BY id DESC;

-- UPDATE — зміна одного повідомлення (приклад: для id = 2)
UPDATE requests
SET message = 'Оновлено: хочу сфінкса 4–6 місяців, цікавить бронювання та доставка.'
WHERE id = 2;

-- DELETE — видалення одного запису (приклад: для id = 3)
DELETE FROM requests
WHERE id = 3;
